export function pliseBracketsFromGrid() {
  const brackets = [];
  let sort = 0;
  for (let ri = 0; ri < PLISE_HEIGHTS_MM.length; ri++) {
    const h = PLISE_HEIGHTS_MM[ri];
    const row = PLISE_PRICE_GRID[ri];
    if (row.length !== PLISE_WIDTHS_MM.length) {
      throw new Error(`Plisé řádek ${ri}: očekáváno ${PLISE_WIDTHS_MM.length} cen, je ${row.length}`);
    }
    for (let ci = 0; ci < PLISE_WIDTHS_MM.length; ci++) {
      brackets.push({
        width_mm_max: PLISE_WIDTHS_MM[ci],
        height_mm_max: h,
        base_price_czk: row[ci],
        sort_order: sort++,
      });
    }
  }
  return brackets;
}
