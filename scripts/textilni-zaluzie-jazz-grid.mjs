export function jazzBracketsForSkupina(surchargePct) {
  const m = 1 + surchargePct / 100;
  const brackets = [];
  let sort = 0;
  for (let ri = 0; ri < JAZZ_HEIGHTS_MM.length; ri++) {
    const h = JAZZ_HEIGHTS_MM[ri];
    const row = JAZZ_EXPERT_GROUP1_GRID[ri];
    if (row.length !== JAZZ_WIDTHS_MM.length) {
      throw new Error(`Řádek ${ri}: špatný počet cen`);
    }
    for (let ci = 0; ci < JAZZ_WIDTHS_MM.length; ci++) {
      const w = JAZZ_WIDTHS_MM[ci];
      const base = row[ci];
      brackets.push({
        width_mm_max: w,
        height_mm_max: h,
        base_price_czk: Math.round(base * m),
        sort_order: sort++,
      });
    }
  }
  return brackets;
}
